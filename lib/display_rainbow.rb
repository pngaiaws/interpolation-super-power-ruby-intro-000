def display_rainbow(test_array)
  puts "R: #{test_array[0]}, "
  puts "O: #{test_array[1]}, "
  puts "Y: #{test_array[2]}, "
  puts "G: #{test_array[3]}, "
  puts "B: #{test_array[4]}, "
  puts "I: #{test_array[5]}, "
  puts "V: #{test_array[6]}, "
end
colors=['red', 'orange', 'yellow', 'green', 'blue', 'indigo', 'violet']
display_rainbow(colors)
